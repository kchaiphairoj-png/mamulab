/**
 * Build a personalized mantra set.
 * @param birthDateISO ISO date string YYYY-MM-DD
 * @param displayName Optional display name (defaults to "คุณ")
 * @param refreshIndex Counter to rotate through mantra variants (0, 1, 2, ...)
 */
export function buildPersonalMantras(
  birthDateISO: string,
  displayName: string,
  refreshIndex: number = 0
): PersonalMantraSet | null {
  const date = new Date(birthDateISO);
  if (Number.isNaN(date.getTime())) return null;
  const root = calculateRootNumber(date);
  const archetype = ARCHETYPE[root];
  const name = (displayName || "คุณ").trim() || "คุณ";

  const pickFrom = (arr: string[], offset: number) =>
    arr[(offset + refreshIndex) % arr.length];

  const mantras: Mantra[] = [
    {
      category: "opening",
      text: replaceName(pickFrom(POOL[root].opening, 0), name),
    },
    {
      category: "closing",
      text: replaceName(pickFrom(POOL[root].closing, 1), name),
    },
    {
      category: "selfCare",
      text: replaceName(pickFrom(POOL[root].selfCare, 2), name),
    },
  ];

  return {
    root,
    planet: archetype.planet,
    archetype: archetype.archetype,
    name,
    mantras,
  };
}

function replaceName(template: string, name: string): string {
  return template.replace(/\{name\}/g, name);
}

